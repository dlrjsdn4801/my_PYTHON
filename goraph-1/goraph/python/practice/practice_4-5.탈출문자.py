# \n : 줄바꿈
print("백문이 불여일견\n백견이 불여일타")

# \", \' : 문장 내 따옴표
print("저는 '이건우'입니다.")
print("저는 \'이건우\'입니다.")

# \\ : 문장 내에서 \
#print("C:\Users\imcom\Documents\GitHub\goraph>") \ 오류
print("C:\\Users\\imcom\\Documents\\GitHub\\goraph>") 

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")  # 'red '<->'pine'

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭 (4칸 띄우기)
print("Red\tApple")